<h4>Freie-Elektronen-Laser (FEL) – Wissenschaftlicher Hintergrund</h4>
    <p> Ein Freie-Elektronen-Laser (FEL) ist eine besonders leistungsstarke Lichtquelle. Er entsteht, indem ein Elektronenstrahl durch ein Magnetfeld – einen sogenannten Undulator – geleitet wird. Das Besondere an einem FEL ist, dass seine Wellenlänge flexibel einstellbar ist und er eine sehr hohe Intensität erreicht. Dadurch eignet er sich für eine Vielzahl wissenschaftlicher Untersuchungen.
    </p>
<h4>Der FEL am Fritz-Haber-Institut</h4>
    <p> Das Fritz-Haber-Institut der Max-Planck-Gesellschaft betreibt in Berlin eine moderne FEL-Anlage, die Strahlung im mittleren Infrarotbereich (4–50 µm) erzeugt. Diese Technologie ermöglicht hochpräzise spektroskopische Untersuchungen an Molekülen, Clustern und Festkörpern.
    </p>
<h4>Technische Merkmale</h4>
    <p>
    ⚪ Elektronenenergie: Bis zu 50 MeV<br>
    ⚪ Makropuls-Energie: Bis zu 100 mJ<br>
    ⚪ Spektralbereich: 4–50 µm (MIR), zukünftig erweiterbar bis 400 µm (THz)<br>
    ⚪ Anwendungsbereiche: Analyse von Biomolekülen, Metallclustern und Festkörpern
    </p>
<h4>Wissenschaftliche Einsatzgebiete</h4>
    <p> 🔬 Vibrationsspektroskopie: Untersuchung der Struktur von Molekülen <br>
        ⚙️ Materialforschung: Analyse von Halbleitermaterialien und anderen Feststoffen<br>
        💥 Action-Spektroskopie: Anregung und Fragmentierung von Molekülen zur Untersuchung chemischer Prozesse
    </p>  

